package Lista5_Fabricio;

public class Sequencia {
    public static void main(String[] args) {
        int i = 1;
        int j = 60;
        
        while (j > 0)
        {
            System.out.println("I=" + i + "\tJ=" + j);
            i += 3;
            j -= 5;
        }
    }

}
